package R3Iteracje;

public class Zadanie03k3 {
    public static void main(String[] args) {
        /*Napisz pogram który za pomocą instrukcji while() oblicza iteracyjnie równanei y=3*x*/
        int i=0;
        while(i<=10){
            System.out.println("Równanie y=3*x dla x=" +i+" wynosi: "+i*3);
            i++;
        }
    }
}
