package R3Iteracje;

public class Zadanie03k4 {
    public static void main(String[] args) {
        /*napisz program, który za pomocą instrukcji for wyświetla liczby od 1 do 20
         */
        for (int i = 1; i <= 20; i++) {
            if(i<20)
                System.out.print(i+",");
            else
                System.out.println(i+".");
        }
    }
}
