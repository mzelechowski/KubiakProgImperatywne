package R3Iteracje;

public class Zadanie03k19 {
    public static void main(String[] args) {
        /* napisz progam wyświetlający tabliczkę mnożenia dla liczb od 1 do 100 z wykorzystaniem
        podwójnej pętli for
         */
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print(i*j +"\t");
            }
            System.out.println();

        }
    }
}
